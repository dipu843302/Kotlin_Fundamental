package com.company.kotlin_operator

fun main(){
    var a=10
    var b=2

    println("s.shl(b): "+a.shl(b))
    println("s.shr(b): "+a.shr(b))
    println("a.ushr(b:) "+a.ushr(b))
    println("a.and(b): "+a.and(b))
    println("a.or(b): "+a.or(b))
    println("a.xor(b): "+a.xor(b))
    println("a.inv(): "+a.inv())
}